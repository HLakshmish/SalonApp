export function SalonInitials({ name }) {
  const parts = (name || 'S').trim().split(/\s+/).filter(Boolean)
  const initials = parts.length >= 2
    ? `${parts[0][0]}${parts[1][0]}`.toUpperCase()
    : (parts[0]?.slice(0, 2) || 'SA').toUpperCase()
  return <span className="salon-initials">{initials}</span>
}
